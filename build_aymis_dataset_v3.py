def build_template_samples(prompts, responses, n, mode="light"):
    out = []
    pool = actions_light if mode == "light" else actions_soft
    for _ in range(n):
        q = random.choice(prompts)
        base = random.choice(responses)
        reply = pack_reply(
            maybe_action(pool, 0.52 if mode == "light" else 0.62) + maybe_opener() + base.split("\n")[0],
            *base.split("\n")[1:]
        )
        out.append((q, reply))
    return out

